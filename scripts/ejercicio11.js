let color = "verde";

switch (color) {
    case "rojo":
        console.log("Detenerse");
        alert("Detenerse");
        break;
        
    case "verde":
        console.log("Avanzar");
        alert("Avanzar");
        break;
        
    default:
        
        console.log("Precaución");
        alert("Precaución");
        break;
}